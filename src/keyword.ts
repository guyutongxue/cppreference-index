export function getStdAttributes(): AttributeIndex[] {
  return STD_ATTRIBUTES.map<AttributeIndex>((a) => ({
    type: "attribute",
    namespace: "",
    name: `[[${a.attr}]]`,
    link: a.link ?? `cpp/language/attributes/${a.attr}`,
    marks: {
      since: a.ts ?? a.since,
    },
  }));
}
