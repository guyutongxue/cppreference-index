export function getKeywords(): KeywordIndex[] {
  return [
    ...KEYWORDS.map<KeywordIndex>((w) => ({
      type: "keyword",
      name: w.keyword,
      marks: {
        since: w.ts ?? w.since,
      },
      link: `cpp/keyword/${w.keyword}`,
      canBeUsedAsIdentifier: false,
    })),
    ...IDENTIFIERS_W_SPECIAL_MEANING.map<KeywordIndex>((w) => ({
      type: "keyword",
      name: w.keyword,
      marks: {
        since: w.ts ?? w.since,
      },
      link: w.link,
      canBeUsedAsIdentifier: false,
    })),
  ];
}

